"""
Functions
"""


def add(a: int, b: int) -> int:
    """
    Return the sum of two numbers
    """
    return a + b


def subtract(a: int, b: int) -> int:
    """
    Return the result of two numbers
    """
    return a - b


def multiply(a: int, b: int) -> int:
    """
    Return the result of two numbers
    """
    return a * b


def divide(a: int, b: int) -> int:
    """
    Return the result of two numbers
    """
    return a / b
